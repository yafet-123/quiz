// Helper functions
export function getAllFlashcardsBySubject() {
  return FlashcardsBySubject;
}

export function getFlashcardsForSubject(subject) {
  return FlashcardsBySubject.find((s) => s.subject === subject)?.topics || [];
}

export function getFlashCardBySubjectAndTitle(subject, topic) {

  const flashcard = FlashcardsBySubject.find(
    (item) => item.subject.toLowerCase() === subject.toLowerCase()
  );

  if (!flashcard) return null;

  // Find flash by title inside that flashcard
  const flash = flashcard.topics.find(
    (q) => q.topic.toLowerCase() === decodeURIComponent(topic).toLowerCase()
  );

  return flash || null;
}